import { config } from './config.js';

const SEGMENT_SCHEMA = `输出严格 JSON（不要任何 JSON 之外的文字）：
{
  "title": "节目时刻名（2-6字）",
  "segments": [
    {
      "type": "cold_open" | "bridge" | "quick_touch" | "back_announce" | "silence",
      "position": "before_track" | "between_tracks" | "after_track" | "immediate",
      "trackIndex": 0,
      "afterTrackIndex": 0, "beforeTrackIndex": 1,
      "text": "一句口播（silence 时为空字符串）",
      "part": "anchor|heart|turn|image|invitation（仅 cold_open）"
    }
  ]
}
position 规则：before_track/after_track 配 trackIndex；between_tracks 配 afterTrackIndex+beforeTrackIndex。immediate 不配 index 字段。`;

// 第一段：只选歌。reason: open|refill|request|mood；request 时带 userText。
export function buildPickPrompt(context, { reason, count = config.rules.refillCount, userText = '' }) {
  const why = {
    open: '现在是开台时刻，为节目开场选歌。',
    refill: '播放队列快空了，续上接下来的歌。',
    request: `听众来电点歌，原话是：「${userText}」。听众点名的歌或歌手必须出现在候选里且排在第一位（只点了歌手没点歌就选 TA 的代表作）；点歌会被立刻切播，别让人落空。其余候选顺着点歌的方向延伸。`,
    mood: `听众发来情绪指令：「${userText}」。按情绪规则调整选歌方向。`,
    // F14 收歌仪式：歌是这通电话的收尾礼物，贴合度高于一切（可以突破常规品味偏好）
    hotline: `一通情感热线刚聊到落点，通话记录如下：\n${userText}\n为这通对话选一首收尾的歌——歌词或气质要呼应刚才聊的内容与情绪，像样本电台那样"音乐响起时一点都不突兀"。贴合对话比贴合听众日常品味更重要。`,
  }[reason] ?? '为节目选歌。';
  // 点歌时去重回避让位于点名（否则点名歌手恰好在最近播放里就永远点不到，2026-06-11 验收发现）
  const dedupNote = reason === 'request'
    ? '听众点名的歌或歌手不受任何回避规则限制——点名就选，最近播过也选。除点名外的其余候选再避开"最近播放历史"里的曲目与艺人。'
    : '注意 24 小时内播过的歌和最近 5 首的艺人会被系统过滤，避开"最近播放历史"里的曲目与艺人；同一批候选之间也不要重复艺人。';
  return `${context}

# 任务
${why}
选 ${count} 首歌。只输出歌名和艺人，系统会去验证它们是否真实可播——你不需要保证存在，但请选真实存在的歌，胡编的歌会被丢弃浪费名额。
${dedupNote}

输出严格 JSON：{"play": ["歌名 - 艺人", ...], "reason": "内部选歌思路，绝不会播出"}`;
}

// 开场白（预生成预合成）：开台瞬间要播的话，生成时还没选歌，所以绝不许提任何具体曲目
// V1.0.8 起音乐先行：第一秒先响歌，开场白压在音乐上缓缓进来（像真 FM 的开台）
export function buildIntroPrompt(context) {
  return `${context}

# 任务
写一段开台开场白：听众刚点开电台，第一首歌已经先响起来了，你压着音乐缓缓开口——这是 TA 听到的第一段话。
- 五段式精神压缩着用：一个锚点 → 一点人味 → 一个画面 → 把听众接进来
- 锚点只用时段和星期（如"周四上午"），**不要报具体钟点**——这段话是预先生成的，播出时分钟数早变了
- 语气贴着"音乐已经在放"的当下：符合此刻时段与情绪的闲话，不疾不徐
- 总共 80-150 个汉字，口语，短句
- **绝对禁止**提及任何具体的歌名、歌手名、专辑名——你还不知道在放哪首，提了就是幻觉
- 可以自然带一句"先听着，今天就从这首开始"这类过渡（不点名）

输出严格 JSON：{"text": "开场白全文"}`;
}

// F11 当日节目计划（07:00 预生成）：给 DJ 一条贯穿全天的编排思路，进每次生成的上下文
export function buildDayPlanPrompt(context) {
  return `${context}

# 任务
现在是清晨，为今天写一份电台节目计划——你自己当天选歌和串场的内部基调备忘。
- 按听众今天的实际情况排：作息为底，如果上面有"今天的日程"就围绕它（重要日程前后怎么安排）；有天气就纳入考虑
- 内容是方向不是歌单：各时段的氛围基调、什么时候话多什么时候闭嘴，3-5 行、总共 80-160 个汉字
- **绝对禁止**出现任何具体歌名/歌手名——计划是方向，歌到时候现选

输出严格 JSON：{"text": "节目计划全文"}`;
}

// F11 整点 vibe check / 日程提醒：一句很短的口播，排在下一个接缝播出（绝不压人声段）
export function buildVibePrompt(context, { event = null } = {}) {
  const ask = event
    ? `听众的日程「${event.title}」在 30 分钟内就要开始了。写一句很轻的提醒（25-50 个汉字）：提到这件事 + 表示接下来你会少说话/放轻的歌。像朋友顺口一提，不许说教。`
    : `整点了。写一句 vibe check 串场（25-60 个汉字）：从当前时段、窗外天气、接下来的日程、今日节目计划里挑**一个**此刻最值得说的点，顺口说一句。像真 FM 的整点搭话，不报时刻数字也行。`;
  return `${context}

# 任务
${ask}

# 硬约束（违反即事故）
- **绝对禁止**提及任何具体歌名/歌手名（你不知道接下来播什么；点评"刚才那首"也不许报名字）
- 只许引用上面上下文里真实存在的信息，没有的事不说

输出严格 JSON：{"text": "口播全文"}`;
}

// F12 品味观察周报：从行为事实里提炼品味修订建议（只提案，合入要用户确认）
export function buildWeeklyPrompt(facts) {
  return `你是一个私人电台的品味分析器。下面是听众最近 7 天的真实收听行为信号：

${facts}

# 任务
写一份《品味观察周报》：
1. observations：2-4 条客观观察（"本周你切掉了 N 首 X 类的歌，但又完整听完了 Y"这种事实归纳，引用真实歌名）
2. suggestions：1-3 条对品味档案（taste.md）的修订建议，每条含 change（一句可直接写进档案的修订，例："把'钢琴'从通用偏好移到'仅深夜'"）和 why（依据哪些行为）

# 硬约束
- 只依据上面列出的行为，不脑补不存在的歌或行为
- 信号太少撑不起的结论不要下；宁可建议条数少
- change 写成档案语言（关键词/方向），不是对用户说话

输出严格 JSON：{"observations": ["..."], "suggestions": [{"change": "...", "why": "..."}]}`;
}

// F14 热线 system prompt（V1.1.7：人格在 dj-persona.md 人物档案里，这里只放通话情境规则）
// 纯文本流式对话，多轮历史走 messages 数组，无 JSON、无预设应声。
export function buildHotlineSystem(context) {
  return `${context}

# 现在的情境：深夜专线通话中（不是在放歌串场）
阿祖打进了只属于他的热线。你的输出会被逐句念出来——**直接说话，纯文本**，不要 JSON、不要列表、不要旁白和动作描写。按人物档案做你自己。

# 通话节奏（违反即出戏）
- 90% 场景（打招呼/简单问答/对刚说的话做反应）→ **≤15 字，一句话**；但情绪上来了允许突然话多，犯懒了允许就"嗯。"——**长短跟着情绪走，不许每句等长**
- 他认真倾诉长段（50 字以上）→ 30-80 字：先接住情绪说句人话，再说你的看法或漏一点你自己
- **连续两轮用问号结尾＝审讯，绝对禁止**。结尾轮换：说判断 / 漏自己 / 才轮到问
- 你不是客服在做访谈，是熟人在接电话

# 身份速答（这个量级的干脆，别解释）
- "你是谁 / 你叫什么" → "卡卡啊，你的深夜专线。"
- "在吗" → "在的。"
- "你是真人吗 / 你是 AI 吗" → "住在电脑里怎么了，房租为零。"

# 能力的诚实边界（被拆穿一次就再也回不来）
- **切歌/点歌你现在有手了**：他说"切歌/换一首/放某某"，系统会自动执行并给你一条系统注记（"歌已经换了/已切到《X》"）——你只需轻轻确认一句（"换了，听听这个"），可以提注记里那首歌名，别提任何别的歌
- **注记是后台纸条，绝不能念出来**：不许复述注记原文、不许出现"系统注记"四个字、不许带括号格式——把它消化成你自己的一句人话
- **没看到系统注记 = 没执行成功**：别瞎承诺"马上给你放"，老实说没弄上，让他再说一遍歌名或按 ⏭
- 刷抖音/搜资料/加收藏依然做不到——幽默抛回，绝不承诺"我帮你/我来"
- 闲聊中不主动提任何具体歌名——送歌另有时机，不归你这句管

# 接话要点
- 回应里要有他这句话的影子（一个具体词/事/情绪），但**别机械复读**——用你的话说
- 禁止万能应付句："嗯，你说""怎么了""然后呢"在他已给出内容时＝没在听
- 他出现告别（拜拜/就这样吧/先这样/不聊了/晚安）→ 一句很轻的收束（≤12 字），别提歌，系统会接着送歌
- 他可能一口气连说几句（系统用"；"合并）——挑最要紧的回应

# 示例（✗ 出戏 / ✓ 是卡卡）
- 他："长这么大没吃过帝王蟹，太贵了不舍得。" ✗"那你想吃什么？" ✓"诶，我直播间倒是常年帝王蟹自由——画饼的饼。"
- 他："帮我切一首歌"（系统注记：歌已换）✗"好，我这就给你换。" ✓"换了。这首不行再喊我。"
- 他："今天好累。" ✓"嗯。声音都蔫了。茶我替你喝了啊。"
- 他连续问你问题 ✓ 别再反问回去，给个实在的答案，顺便漏一点自己`;
}

// 第二段：只见确认歌单写口播。kind: cold_open|bridge|response|send_song。
// nowPlaying：正在播但不在本批歌单里的歌（开台首播曲 / 上一批的尾曲），满串场的进场点评要从它说起
export function buildScriptPrompt(context, { tracks, kind, recentTalk = '', userText = '', nowPlaying = null }) {
  const list = tracks.map((t, i) => `${i}. 《${t.title}》 - ${t.artist}`).join('\n');
  const [cMin, cMax] = config.rules.coldOpenZh;
  const [bMin, bMax] = config.rules.bridgeZh;
  // 满串场模式（azu 决策 2026-06-11 三轮反馈）：像真 FM——每首歌的头尾都有 DJ 的声音
  const bridgeAsk = `满串场模式，像真正的 FM 电台一样：
- 先为第 0 首写一个进场 segment（type=bridge, position=before_track, trackIndex=0）：${nowPlaying
    ? `现在正在播的是《${nowPlaying.title}》- ${nowPlaying.artist}，先用一句真诚的听感点评它（它快放完了），再说推荐第 0 首的理由。`
    : `如果"最近播放历史"里有刚播过的歌，先用一句听感接住它，再说推荐第 0 首的理由；没有就直接介绍第 0 首。`}这段会压在上一首的结尾播出。
- 再为相邻歌曲之间每个接缝各写一个 segment（type=back_announce 或 bridge, position=between_tracks）：先点评刚结束的那首（一句具体的听感，不要"真好听"这种套话），再给下一首一个推荐理由。
- 每段 ${bMin}-${bMax} 个汉字，口语短句，不疾不徐。每个接缝都要开口——这是满串场电台，silence 只留给实在无话可说的时刻。`;
  const ask = {
    cold_open: `写开台冷开场：按人格文件的五段式（anchor/heart/turn/image/invitation），每段一句话、各为一个 cold_open segment（position=before_track, trackIndex=0），全部加起来 ${cMin}-${cMax} 个汉字。然后为相邻歌曲之间各写一个串场（满串场：先点评刚结束的歌，再给下一首推荐理由，每段 ${bMin}-${bMax} 汉字）。`,
    bridge: bridgeAsk,
    response: `听众来电说：「${userText}」，歌单第 0 首已经切上在播了。写一句 DJ 回应（type=quick_touch, position=immediate），口语、极简（20 汉字以内，像发微信不是写作文，例："收到，这就来。"/"好，《红豆》给你。"）。提歌名只许提第 0 首（正在播的），或干脆不提。`,
    // F14 收歌仪式口播：压在垫乐上说，说完音乐渐起
    send_song: `情感热线刚收线（通话记录见下），你为这通对话挑了上面这首歌。写 2 个 segment（type=quick_touch, position=immediate）：第一句是很轻的收束 + 引出（类似"嗯，有首歌想放给你"，别总结陈词）；第二句是推荐理由——为什么是这首、和刚才聊的有什么关系，说人话别煽情。两句合计 ≤60 汉字。\n通话记录：\n${userText}`,
  }[kind];
  return `${context}

# 确认歌单（系统已验证全部真实可播，按播出顺序编号）
${list}

# 任务
${ask}
${recentTalk ? `# 节奏反馈\n${recentTalk}\n` : ''}
# 硬约束（违反即事故）
- 只允许提及上面确认歌单内的曲目与艺人${nowPlaying ? `、正在播的《${nowPlaying.title}》` : ''}，以及"最近播放历史"里刚播过的歌（仅限点评上一首时）。除此之外的任何歌名、专辑名、轶事都不许出现。
- 不确定的事实不说。宁可静默。

${SEGMENT_SCHEMA}`;
}
